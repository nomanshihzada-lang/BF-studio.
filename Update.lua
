-- Random Name Generator (رینڈم نام بنانے کا سسٹم)
math.randomseed(os.time())
local function generateRandomName()
    local adjs = {"Pro", "Epic", "Dark", "Ghost", "Ninja", "Super", "Toxic", "Shadow", "Alpha", "Hyper"}
    local nouns = {"Gamer", "Sniper", "King", "Wolf", "Hunter", "Player", "Hero", "Beast", "Titan", "Viper"}
    return adjs[math.random(#adjs)] .. nouns[math.random(#nouns)] .. math.random(10, 99)
end

-- User Preferences (ڈیٹا سیو کرنے کے لیے)
local prefs = this.getSharedPreferences("DCS_Studio_Prefs", 0)
local editor = prefs.edit()

local savedUser = prefs.getString("username", "")
