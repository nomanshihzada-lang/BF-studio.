-- 5. TTS Settings Menu
showTTS = function()
    local tts_labels, tts_pkgs = {}, {}
    pcall(function()
        local pm = this.getPackageManager()
        local res = pm.queryIntentServices(Intent("android.intent.action.TTS_SERVICE"), 0)
        for i=0, res.size()-1 do
            local info = res.get(i)
            table.insert(tts_labels, tostring(info.loadLabel(pm)))
            table.insert(tts_pkgs, info.serviceInfo.packageName)
        end
    end)
    
    local l = {
        ScrollView,
        layout_width="fill",
        layout_height="fill",
        {
            LinearLayout,
            orientation="vertical",
            layout_width="fill",
            padding="20dp",
            {
                CheckBox,
                id="t_on",
                text="TTS On/Off",
                checked=isTtsOn,
                onClick=function(v)
                    isTtsOn = v.isChecked()
                    editor.putBoolean("tts_on", isTtsOn)
                    editor.commit()
                    if isTtsOn then announce("TTS is On") end
                end
            },
            {
                Spinner,
                id="t_spin",
                layout_width="fill",
                layout_marginTop="10dp"
            },
            { TextView, text="Rate", layout_marginTop="10dp" },
            { SeekBar, id="s_rate", max=100, progress=savedRate, layout_width="fill" },
            
            { TextView, text="Volume", layout_marginTop="10dp" },
            { SeekBar, id="s_vol", max=100, progress=savedVol, layout_width="fill" },
            
            { TextView, text="Pitch", layout_marginTop="10dp" },
            { SeekBar, id="s_pitch", max=100, progress=savedPitch, layout_width="fill" },
            
            {
                LinearLayout,
                orientation="horizontal",
                layout_width="fill",
                layout_marginTop="20dp",
                {
                    Button,
                    text="Test",
                    layout_weight="1",
                    onClick=function()
                        if isTtsOn then announce("Testing TTS Settings") else service.speak("Please turn on TTS first") end
                    end
                },
                {
                    Button,
                    text="Save",
                    layout_weight="1",
                    onClick=function()
                        editor.putInt("tts_rate", s_rate.getProgress())
                        editor.putInt("tts_pitch", s_pitch.getProgress())
                        editor.putInt("tts_vol", s_vol.getProgress())
                        editor.commit()
                        announce("Settings Saved")
                    end
                },
                {
                    Button,
                    text="Reset",
                    layout_weight="1",
                    onClick=function()
                        s_rate.setProgress(50)
                        s_pitch.setProgress(50)
                        s_vol.setProgress(100)
                        announce("Settings Reset")
                    end
                }
            },
            {
                Button,
                text="Back",
                layout_width="fill",
                layout_marginTop="20dp",
                onClick=function() showSettings() end
            }
        }
    }
    root_view.removeAllViews()
    root_view.addView(loadlayout(l))
    
    local adapter = ArrayAdapter(this, android.R.layout.simple_spinner_item, String(tts_labels))
    adapter.setDropDownViewResource(android.R.layout.simple_spinner_dropdown_item)
    t_spin.setAdapter(adapter)
    
    local sel_index = 0
    for i, pkg in ipairs(tts_pkgs) do
        if pkg == selectedEngine then
            sel_index = i - 1
        end
    end
    t_spin.setSelection(sel_index)

    t_spin.setOnItemSelectedListener(AdapterView.OnItemSelectedListener{
        onItemSelected=function(p,v,pos,id)
            local pkg = tts_pkgs[pos+1]
            if pkg ~= selectedEngine then
                selectedEngine = pkg
                editor.putString("tts_engine", pkg)
                editor.commit()
                setupTTS(pkg)
            end
        end,
        onNothingSelected=function(p) end
    })

    s_rate.setOnSeekBarChangeListener(SeekBar.OnSeekBarChangeListener{
        onProgressChanged = function(seekBar, progress, fromUser)
            if fromUser then
                savedRate = progress
                if customTTS then customTTS.setSpeechRate(progress / 50) end
            end
        end,
        onStartTrackingTouch = function(seekBar) end,
        onStopTrackingTouch = function(seekBar) end
    })

    s_pitch.setOnSeekBarChangeListener(SeekBar.OnSeekBarChangeListener{
        onProgressChanged = function(seekBar, progress, fromUser)
            if fromUser then
                savedPitch = progress
                if customTTS then customTTS.setPitch(progress / 50) end
            end
        end,
        onStartTrackingTouch = function(seekBar) end,
        onStopTrackingTouch = function(seekBar) end
    })

    s_vol.setOnSeekBarChangeListener(SeekBar.OnSeekBarChangeListener{
        onProgressChanged = function(seekBar, progress, fromUser)
            if fromUser then
                savedVol = progress
            end
        end,
        onStartTrackingTouch = function(seekBar) end,
        onStopTrackingTouch = function(seekBar) end
    })
end

