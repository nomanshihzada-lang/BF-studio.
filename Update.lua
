-- TTS Settings
local selectedEngine = prefs.getString("tts_engine", "")
local savedRate = prefs.getInt("tts_rate", 50)
local savedPitch = prefs.getInt("tts_pitch", 50)
local savedVol = prefs.getInt("tts_vol", 100)

-- Custom TTS Setup
local customTTS = nil
local function setupTTS(enginePkg)
    if customTTS ~= nil then
        pcall(function() customTTS.shutdown() end)
    end
    
    local listener = TextToSpeech.OnInitListener{
        onInit = function(status)
            if status == TextToSpeech.SUCCESS and customTTS ~= nil then
                customTTS.setSpeechRate(savedRate / 50)
                customTTS.setPitch(savedPitch / 50)
            end
        end
    }
    
    if enginePkg == "" or enginePkg == nil then
        customTTS = TextToSpeech(this, listener)
    else
        customTTS = TextToSpeech(this, listener, enginePkg)
    end
end

setupTTS(selectedEngine)

local function announce(txt)
    if isTtsOn then
        if customTTS ~= nil then
            pcall(function()
                local params = Bundle()
                params.putFloat("volume", savedVol / 100)
                customTTS.speak(txt, 0, params, "dcs_announce")
            end)
        else
            service.speak(txt)
        end
    end
end

